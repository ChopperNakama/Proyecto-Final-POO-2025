    private void mostrarTablaOrdernada() {
        modelo.setRowCount(0);
        Set<String> listaproductos = deposito.productos();
        List<Object[]> filas = new ArrayList();
        for (String producto : listaproductos) {
            Object[] fila = {producto, deposito.precio(producto), deposito.stock(producto)};
            filas.add(fila);
        }
        filas.stream()
                .sorted((f1, f2) -> Integer.compare((int) f1[2], (int) f2[2]))
                .forEach(fila -> modelo.addRow(fila));
    }
