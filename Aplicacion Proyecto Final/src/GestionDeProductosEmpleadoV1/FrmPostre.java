    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        String inputNombre = JOptionPane.showInputDialog(null, "Ingrese Nombre:");
        String ans = deposito.buscar(inputNombre);
        if (ans != null) {

            JOptionPane.showMessageDialog(this, ans);
        } else {
            JOptionPane.showMessageDialog(null, "Producto no encontrado con Nombre: " + inputNombre, "No encontrado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

